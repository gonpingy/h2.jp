            <h3><span class="acover2"></span>住まい手の幸せも、<span class="sp_i"><br></span>つくり手の幸せも</h3>
          </div>
          <div class="v_txt abox"><span class="acover"></span>
            <p>住まい手が幸せな家づくりを目指すのは当然だけど、つくり手はどうだろう？</p>
            <p>HandiHouse projectが始まったのは、それぞれ設計や施工の仕事に携わっていたメンバーが一様に抱いていた「僕たちは、誰のために、何をつくっているんだろう？」という疑問がきっかけでした。</p>
            <p>細かく分業化され、受身受身で作業をこなさざるを得ないつくり手たち。ただただ工期に追われながら仕事が進み、精魂込めて仕上げた空間を喜んでくれる住まい手の顔を見ることもない。そんな家づくりは、果たして「いい家づくり」なのだろうか。</p>

