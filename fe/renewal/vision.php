                <p>そして、住まい手と一緒に家づくりに取り組むつくり手を全国に増やし、ネットワーク化していくことも、僕らの新たなビジョンのひとつ。つくり手たちがこれからの生業のつくり方と在り方を考え、活躍の場を広げていくための活動も行なっていきます。</p>
