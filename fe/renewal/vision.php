                <p>たとえば「寸法決め」という作業ひとつをみても、現場でお客さんと一緒になって、「ここ？」「もうちょっとこっち」と相談しながら決められれば、図面をつくる作業が簡素化できるし、クレームが出ることも避けられる。</p>
                <p>HandiHouse projectの家づくりでは、設計プランも、使う素材も、すべてを住まい手と直接話し合って決めていきます。でも、すべて住まい手の言う通りにつくるわけではなく、「こうしたらもっといい」「こっちのほうが面白い」という僕らの思いを伝えて、お互いぶつかり合いながらつくっていくスタイル。</p>
                <p>実際につくる人とコミュニケーションしながら取り組むから、住まい手は意見が言いやすいし、そうなるほど僕らも言いやすい。家は住まい手のものだけど、その家づくりは僕らつくり手にとっても「自分ごと」だから。</p>
                <p>つくり手が楽しめなければ、住まい手だって楽しめない。<br>だから僕たちは、つくり手も幸せな家づくりがしたい。</p>
                <p>住まい手も、設計士も、大工も、塗装屋も、電気屋も、水道屋も。<br>みんなが同じ「家づくり」という舞台に立つプレイヤーであり、それぞれがエンターテイナー。<br>そんなプレイヤーたちのセッションが生み出す、世界にひとつだけの家。</p>
                <p>住まい手は、誰が家をつくるのかを知り、どうできあがるのかを知ることで、<br>住まいへの愛着だけでなく、住まいを住みこなしていく力も育つ。<br>つくり手は、自分の仕事が誰にどう喜ばれるのかを知り、自分の仕事の価値への誇りが育つ。</p>
                <p>「つくり手も幸せな家づくり」を広げていくことは、<br>これから先の住まい手たちの幸せな家づくりの土壌を育むことにもつながる。<br>HandiHouse projectが目指すのはそんな、住まい手もつくり手も幸せな家づくりです。</p>
