          </div>
          <div class="v_txt abox"><span class="acover"></span>
            <p>2011年のスタート時からこれまで、HandiHouse projectはメンバーそれぞれが独立した個人事業主で、家づくりという「project」ごとに、住まい手と職人さんとチームを組んで活動をしてきました。</p>
            <p>2018年夏、HandiHouse projectを会社化すると決めた時、「project」を残すか残さないかについて、メンバー内で議論になりました。そうして決まった会社名は、「株式会社HandiHouse project」。</p>
            <p>なぜ「project」を残したかというと、僕らがやりたいことは「家をつくること」ではなく、「住まい手もつくり手も幸せな家づくりの形を、もっともっと世の中に広めていくこと」だから。</p>
            <p>これからのHandiHouse projectは、ひとつひとつの家づくりを通しての活動も継続しながら、<br>HandiLaboを実験・検証・発信の場として、「家づくりを楽しむ文化」を育てていきたいと考えています。</p>

